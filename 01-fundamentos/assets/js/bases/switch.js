const dia = '0';
// El Switch evalua la condicion con un ===
switch ( dia ) {
    case '0':
        console.log('Domingo');
        break;
    case 1:
        console.log('Lunes');
        break;
    case 2:
        console.log('Martes');
        break;
    default:
        console.log('No es domingo o lunes o martes')
        break;
}


